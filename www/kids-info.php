Для новых владельцев наших малышей мы проводим подробные консультации по вопросам питания и содержания животного, о том, как надо подготовится к приезду котенка. Мы рады поделиться с Вами опытом по воспитанию малыша, отвечаем на любые вопросы по телефону, переписываемся по интернету, даем рекомендации по выставочной карьере (стерилизованные животные могут принимать участие в выставках!!!). А также мы всегда рады получать фото наших выпускников!!!

Обратите внимание: 
1.	Мы не отвечаем на письма и звонки, состоящие из одной фразы о стоимости котенка мейн-кун. Напишите нам немного о себе, цели приобретения котенка, ваших животных, если они есть и опыте содержания кошек, желательном характере или окрасе будущего питомца. Если вы выбрали себе котенка по фото, мы ответим на вопросы о конкретном малыше и с удовольствием договоримся о встрече с Вами.
2.	Мы очень заботимся о наших малышах и оставляем за собой право выбора их будущих владельцев, это значит, что мы можем отказать в приобретении котенка в нашем питомнике без объяснения причин. 
3.	Так ка решение о приобретении котенка должно быть обдуманным и взвешенным, при отказе покупателя от котенка залог не возвращается ни при каких обстоятельствах.

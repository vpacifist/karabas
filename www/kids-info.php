Продан – котенок продан и уезжает в новый дом.
Остается с нами – котенок остается в питомнике

Продажа котят в разведение ограничена и возможна только в зарегистрированные питомники. 

Котята переезжают в новый дом не ранее 3-х – 3,5 месяцев, после двухкратной комплексной вакцинации, дегельминтизации и прохождения карантина. Малыши к этому возрасту уже социализированы и достаточно самостоятельны, приучены к когтеточке и лотку.

Котята, проданные как домашние любимцы переезжают в новый дом уже стерилизованными (мальчики–коты будут готовы к переезду через 5-ть дней, девочки–через10-ть дней после стерилизации).

Срок резервирования и полной оплаты устанавливается по договоренности сторон, но не позднее чем за две недели до переезда котенка в новый дом.
